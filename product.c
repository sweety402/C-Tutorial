// write a program to return product of two number 
#include "stdio.h"
int main()
{
    int a;
    int b;
    printf("enter first number = ");
    scanf("%d",&a);
    printf("enter second number = ");
    scanf("%d",&b);
    printf("product:%d",a*b);
    return 0;
}


// output
// enter first number = 20
// enter second number = 5
// product:100
